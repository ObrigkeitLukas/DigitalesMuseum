          Maria Montessori * 31.08.1870  Chiaravalle, gestorben  am 06. 05.1952 in Noordwijk aan Zee
          <br/>
          <br/>
          <strong style="font-size: 28px">"Hilf mir, es selbst zu tun!" (Montessori, Maria 2007)</strong>
          <br/>
          <br/>
          Maria Montessori wurde am 31.8.1870 in Chiaravalle bei Ancona in Italien geboren. Ihr Vater war in der Tabakindustrie tätig, und ihre Mutter war Hausfrau. Die Familie zog nach Rom. Ihre Eltern wünschten, dass sie Lehrerin wird, doch Maria wollte Ärztin werden. Als eine der ersten Frauen studierte sie ab 1890 in Rom Naturwissenschaften und promovierte 1896 im Fachgebiet Psychiatrie.1904 habilitierte sie sich  im Fach Pädagogische Anthropologie. 1898 brachte sie heimlich ihren unehelichen Sohn Mario zur Welt und gab ihn freiwilligin eine Pflegefamilie, denn ihr drohte ansonsten der Verlust des Kindes, soziale Ächtung und  Armut. So aber hielt sie Kontakt zu ihm. Als Mario 14 Jahre alt war, lebten sie zusammen in Barcelona.Fortan war Mario ihr fester Begleiter, wenn sie in Europa, Südamerika und Indien unterwegs war, um Vorträge und Kurse in Montessori-Pädagogik anzubieten. 1936 wechselten beide den Wohnort. Um dem spanischen Bürgerkrieg zu entkomme,  zogen sie nach Amsterdam. Der Ausbruch des Zweiten Weltkrieges trug dazu bei, dass sie Indien nicht verlassen konnten. Maria Montessori  kehrte erst 1946 nach Europa zurück und lebte bis zu ihrem Tode in Noordwijk aan Zee in den Niederlanden.
          <br/>
          <br/>
          Weltweit gelang es Maria Montessori pädagogische Institutionen zu iniitiieren und der Heil-,  Schul-, und Sozialpädagogik neue Impulse zu geben. Sie bildete Erzieherinnen und Lehrer und Lehrerinnen aus. 1914 bot sie ihren ersten internationalen Ausbildungskurs an. Im Armenviertel San Lorenzo in Rom war es ihr ab 1907 gelungen, durch die Betreuung eines Projektesdie kindliche Entwicklung näher zu erforschen. In dem neugegründeten Kinderhaus Casa Bambini wurden behinderte und verwahrloste Kinder im schulfähigen Alter betreut und mithilfe von besonderen Materialien in ihrer sinnlichen und geistigen Entwicklung gefördert.Die von Montessori eingesetzten Arbeitsmaterialen trugen dazu bei, dass die Kinder Entwicklungsfortschritte machten, und ihre Methode Beachtung fand. 1923 wandte sie sich an Mussolini, ihre Erziehungsmethode in italienischen Schulen zuzulassen. Dazu gründete sie auch den Verein "Opera Montessori". Ihre Gedanken über Pädagogik sollten sich verbreiten. Mussolini war 1924 zum Ehrenvorsitzenden des Vereins ernannt worden.Ihr späteres Engagement für Friedenserziehung führte zum Bruch mit dem Diktator. Ab 1932 wurden Mario und seine Mutter durch die italienische Geheimpolizei überwacht.
